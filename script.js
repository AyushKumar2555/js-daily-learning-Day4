// Prefix and Postfix Increment/Decrement in JavaScript

let a = 5;
console.log("Initial value of a:", a);

// Postfix Increment
console.log("Postfix Increment (a++):", a++); // Outputs 5, then a becomes 6
console.log("After Postfix Increment, a:", a); // Now a is 6

// Prefix Increment
console.log("Prefix Increment (++a):", ++a); // a becomes 7, then outputs 7

// Postfix Decrement
console.log("Postfix Decrement (a--):", a--); // Outputs 7, then a becomes 6
console.log("After Postfix Decrement, a:", a); // Now a is 6

// Prefix Decrement
console.log("Prefix Decrement (--a):", --a); // a becomes 5, then outputs 5
